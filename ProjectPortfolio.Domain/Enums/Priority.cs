namespace ProjectPortfolio.Domain.Enums
{
    // Enum representing the priority levels for tasks, issues, or projects
    public enum Priority
    {
        // Lowest priority – typically non-urgent and minimal impact
        Low = 1,

        // Moderate priority – should be addressed in a reasonable timeframe
        Medium = 2,

        // High priority – important and time-sensitive, requires prompt attention
        High = 3,

        // Highest priority – critical issue with major impact, demands immediate action
        Critical = 4
    }
}
